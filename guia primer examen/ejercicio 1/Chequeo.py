class chequeo(object):
    peso = None
    altura = None
    fecha = None

    def setPeso(self,peso):
        self.peso = peso

    def getPeso(self):
        return self.peso

    def setAltura(self,altura):
        self.altura = altura

    def getAltura(self):
        return self.altura

    def setFecha(self,fecha):
        self.fecha = fecha

    def getFecha(self):
        return self.fecha