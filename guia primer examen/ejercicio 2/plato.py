class Plato(object):
    nombre_plato = None
    calorias = None

    def setNombrePlato(self,nombre_plato):
        self.nombre_plato = nombre_plato

    def getNombrePlato(self):
        return self.nombre_plato

    def setCalorias(self,calorias):
        self.calorias = calorias

    def getCalorias(self):
        return self.calorias

